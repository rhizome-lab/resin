//! Marching cubes algorithm for generating meshes from signed distance fields.
//!
//! # Example
//!
//! ```ignore
//! use resin_mesh::{marching_cubes, MarchingCubesConfig};
//! use glam::Vec3;
//!
//! // Define a sphere SDF
//! let sdf = |p: Vec3| p.length() - 1.0;
//!
//! // Generate mesh
//! let mesh = marching_cubes(&sdf, MarchingCubesConfig::default());
//! ```

use glam::Vec3;

use crate::Mesh;

/// Configuration for marching cubes.
#[derive(Debug, Clone)]
pub struct MarchingCubesConfig {
    /// Bounds of the sampling volume (min corner).
    pub min: Vec3,
    /// Bounds of the sampling volume (max corner).
    pub max: Vec3,
    /// Resolution (number of cells) in each dimension.
    pub resolution: usize,
    /// Iso-value at which to extract the surface (default: 0.0).
    pub iso_value: f32,
}

impl Default for MarchingCubesConfig {
    fn default() -> Self {
        Self {
            min: Vec3::splat(-1.0),
            max: Vec3::splat(1.0),
            resolution: 32,
            iso_value: 0.0,
        }
    }
}

impl MarchingCubesConfig {
    /// Creates config with specified bounds.
    pub fn with_bounds(min: Vec3, max: Vec3) -> Self {
        Self {
            min,
            max,
            ..Default::default()
        }
    }

    /// Sets resolution.
    pub fn with_resolution(mut self, resolution: usize) -> Self {
        self.resolution = resolution;
        self
    }

    /// Sets iso-value.
    pub fn with_iso_value(mut self, iso_value: f32) -> Self {
        self.iso_value = iso_value;
        self
    }
}

/// Generates a mesh from a signed distance field using marching cubes.
///
/// The SDF should return negative values inside the surface and positive outside.
pub fn marching_cubes<F>(sdf: F, config: MarchingCubesConfig) -> Mesh
where
    F: Fn(Vec3) -> f32,
{
    let res = config.resolution;
    if res < 2 {
        return Mesh::new();
    }

    let cell_size = (config.max - config.min) / res as f32;

    // Sample the SDF at all grid points
    let mut values = vec![0.0f32; (res + 1) * (res + 1) * (res + 1)];

    for z in 0..=res {
        for y in 0..=res {
            for x in 0..=res {
                let p = config.min + cell_size * Vec3::new(x as f32, y as f32, z as f32);
                let idx = x + y * (res + 1) + z * (res + 1) * (res + 1);
                values[idx] = sdf(p);
            }
        }
    }

    let mut positions = Vec::new();
    let mut indices = Vec::new();

    // Process each cell
    for z in 0..res {
        for y in 0..res {
            for x in 0..res {
                let corners = [
                    Vec3::new(x as f32, y as f32, z as f32),
                    Vec3::new(x as f32 + 1.0, y as f32, z as f32),
                    Vec3::new(x as f32 + 1.0, y as f32, z as f32 + 1.0),
                    Vec3::new(x as f32, y as f32, z as f32 + 1.0),
                    Vec3::new(x as f32, y as f32 + 1.0, z as f32),
                    Vec3::new(x as f32 + 1.0, y as f32 + 1.0, z as f32),
                    Vec3::new(x as f32 + 1.0, y as f32 + 1.0, z as f32 + 1.0),
                    Vec3::new(x as f32, y as f32 + 1.0, z as f32 + 1.0),
                ];

                let mut corner_values = [0.0f32; 8];
                for (i, c) in corners.iter().enumerate() {
                    let ix = c.x as usize;
                    let iy = c.y as usize;
                    let iz = c.z as usize;
                    let idx = ix + iy * (res + 1) + iz * (res + 1) * (res + 1);
                    corner_values[i] = values[idx];
                }

                // Determine cube index based on which corners are inside
                let mut cube_index = 0u8;
                for (i, &v) in corner_values.iter().enumerate() {
                    if v < config.iso_value {
                        cube_index |= 1 << i;
                    }
                }

                // Skip empty cells
                if cube_index == 0 || cube_index == 255 {
                    continue;
                }

                // Get edge table entry
                let edges = EDGE_TABLE[cube_index as usize];
                if edges == 0 {
                    continue;
                }

                // Compute vertex positions for intersected edges
                let mut edge_vertices = [Vec3::ZERO; 12];

                for edge in 0..12 {
                    if edges & (1 << edge) != 0 {
                        let (v0, v1) = EDGE_VERTICES[edge];
                        let p0 = config.min + cell_size * corners[v0];
                        let p1 = config.min + cell_size * corners[v1];
                        let val0 = corner_values[v0];
                        let val1 = corner_values[v1];

                        let t = if (val1 - val0).abs() > 1e-6 {
                            (config.iso_value - val0) / (val1 - val0)
                        } else {
                            0.5
                        };

                        edge_vertices[edge] = p0.lerp(p1, t);
                    }
                }

                // Generate triangles
                let tri_table = &TRI_TABLE[cube_index as usize];
                let mut i = 0;
                while i < 16 && tri_table[i] != -1 {
                    let base = positions.len() as u32;

                    positions.push(edge_vertices[tri_table[i] as usize]);
                    positions.push(edge_vertices[tri_table[i + 1] as usize]);
                    positions.push(edge_vertices[tri_table[i + 2] as usize]);

                    indices.push(base);
                    indices.push(base + 1);
                    indices.push(base + 2);

                    i += 3;
                }
            }
        }
    }

    let mut mesh = Mesh::new();
    mesh.positions = positions;
    mesh.indices = indices;
    mesh.compute_smooth_normals();

    mesh
}

